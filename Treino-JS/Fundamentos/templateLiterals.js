/* A expressão ${} é chamada de template literals em JavaScript.
É uma sintaxe que permite a interpolação de variáveis e expressões em strings.
O uso de template literals é uma maneira mais fácil e legível de concatenar strings e variáveis em JavaScript.

Por exemplo, se você quiser exibir o valor de uma variável x em uma string,
você pode usar a sintaxe ${x}. O valor da variável x será interpolado na string.

Aqui está um exemplo de como usar template literals em JavaScript: */

const name = 'John';
const age = 30;
const message = `My name is ${name} and I am ${age} years old.`;
console.log(message);

// My name is John and I am 30 years old.

const x = 10;
const y = 20;
const sum = `The sum of ${x} and ${y} is ${x + y}.`;
console.log(sum);

// The sum of 10 and 20 is 30.

